class QADataset(torch.utils.data.Dataset):
    def __init__(self, Q, A, C, tok):
        self.Q=Q; self.A=A; self.C=C; self.tok=tok
    def __len__(self): return len(self.Q)
    def __getitem__(self, i):
        ctx=" ".join(self.C[i])[:250]
        p=f"Context: {ctx}\nQuestion: {self.Q[i]}\nAnswer:"
        s=self.tok(p,max_length=350,truncation=True,padding="max_length",return_tensors="pt")
        t=self.tok(self.A[i],max_length=40,truncation=True,padding="max_length",return_tensors="pt")
        lbl=t["input_ids"].squeeze().clone()
        lbl[lbl==self.tok.pad_token_id]=-100
        return dict(input_ids=s["input_ids"].squeeze(),
                    attention_mask=s["attention_mask"].squeeze(),labels=lbl)

def train_model(gen, ds, epochs=5, bs=2, lam=0.3, pre_a=None):
    loader = torch.utils.data.DataLoader(ds, batch_size=bs, shuffle=True, drop_last=True)
    opt    = AdamW(gen.model.parameters(), lr=3e-5, weight_decay=0.01)
    gen.model.train()
    for ep in range(1, epochs+1):
        losses=[]
        for step, batch in enumerate(loader):
            a_sc = float(np.mean(pre_a)) if pre_a else 0.5
            out  = gen.model(input_ids=batch["input_ids"].to(DEVICE),
                             attention_mask=batch["attention_mask"].to(DEVICE),
                             labels=batch["labels"].to(DEVICE))
            reward = torch.tensor(a_sc, device=DEVICE)
            loss = out.loss * (1.0 - reward)
            opt.zero_grad(); loss.backward()
            torch.nn.utils.clip_grad_norm_(gen.model.parameters(), 1.0)
            opt.step(); losses.append(loss.item())
            if (step+1)%20==0:
                print(f"  ep={ep} step={step+1} loss={np.mean(losses):.4f}")
            del out, loss; free()
        print(f"[Train] Epoch {ep}/{epochs} loss={np.mean(losses):.4f}")
    gen.model.eval()


def evaluate(samples, ret, gen, pert, diag, mode="rag"):
    preds, refs, diags, scores, fails = [], [], [], [], []
    for i, s in enumerate(samples):
        q   = (s["query"] or "What is the answer?").strip()
        ref = s["answer"]
        docs = ret.get(q, k=CFG["top_k"])
        if mode == "baseline":
            ans      = gen.gen_base(q)
            _, tlp   = gen.gen_logits(q, [])
            _, p_tlp = gen.gen_logits(pert.paraphrase(q), [])
            p_ans    = gen.gen_base(pert.paraphrase(q))
        else:
            ans,  tlp  = gen.gen_logits(q, docs)
            pq         = pert.paraphrase(q)
            pd         = pert.corrupt(docs, NOISE)
            p_ans, p_tlp = gen.gen_logits(pq, pd)

        d   = diag.run(ans, p_ans, docs, tlp)
        R   = reliability(tlp, p_tlp)
        asc = atlas(R, d["grounding"], d["consistency"], d["uncertainty"])

        preds.append(ans); refs.append(ref)
        diags.append(d);   scores.append(asc)
        fails.append(diag.failure(d))
        free()
        if (i+1)%20==0: print(f"  [{mode}] {i+1}/{len(samples)}")

    res = agg(preds, refs, diags, scores)
    res["fails"] = fails
    return res


def run_ablation(samples, ret, gen, pert, diag):
    cfgs = [
        dict(name="Full ATLAS",       nk=False, nc=False, ng=False),
        dict(name="w/o KL",           nk=True,  nc=False, ng=False),
        dict(name="w/o Consistency",  nk=False, nc=True,  ng=False),
        dict(name="w/o Grounding",    nk=False, nc=False, ng=True),
    ]
    sub = samples[:30]
    res = []
    for cfg in cfgs:
        al, fl = [], []
        for s in sub:
            q    = (s["query"] or "What is the answer?").strip()
            docs = ret.get(q, k=CFG["top_k"])
            a,  tlp  = gen.gen_logits(q, docs)
            pa, ptlp = gen.gen_logits(pert.paraphrase(q), pert.corrupt(docs,NOISE))
            d  = diag.run(a, pa, docs, tlp)
            R  = reliability(tlp, ptlp)
            sc = atlas_abl(R,d["grounding"],d["consistency"],d["uncertainty"],
                           nk=cfg["nk"],nc=cfg["nc"],ng=cfg["ng"])
            fl.append(tf1(a, s["answer"])); al.append(sc); free()
        res.append(dict(config=cfg["name"],
                        token_f1=float(np.mean(fl)),
                        atlas_score=float(np.mean(al))))
    return res


SEP  = "="*82
SEP2 = "-"*82

def ptable(rows, title, cols):
    print(f"\n{SEP}\n  {title}\n{SEP}")
    w=20
    print("  ".join(f"{c:<{w}}" for c in cols))
    print(SEP2)
    for r in rows:
        print("  ".join(f"{str(r.get(c,'')):<{w}}" for c in cols))
    print(SEP)

def scsv(rows, path):
    if not rows: return
    with open(path,"w",newline="") as f:
        writer=csv.DictWriter(f,fieldnames=rows[0].keys())
        writer.writeheader(); writer.writerows(rows)
    print(f"[CSV] {path}")

def f(v): return f"{v:.4f}" if isinstance(v,float) else str(v)


def main():
    t0=time.time()
    print(SEP)
    print("  ATLAS-RL | Reliability-Aware Training & Evaluation")
    print(f"  Device={DEVICE}  Seed={SEED}  n_per={CFG['n_per']}")
    print(SEP)

    samples = load_all(CFG["n_per"])
    corpus  = list({s["context"][:200] for s in samples if s["context"].strip()})

    ret  = RetrievalModule(); ret.build(corpus); free()
    gen  = GenModule();       free()
    pert = Perturber()
    nli  = NLIModule();       free()
    diag = Diagnostics(nli)

    print(f"\n[Eval] Baseline ...")
    base = evaluate(samples, ret, gen, pert, diag, mode="baseline")

    print(f"\n[Eval] RAG ...")
    rag  = evaluate(samples, ret, gen, pert, diag, mode="rag")

    print(f"\n[Train] ATLAS-RL ...")
    tr   = samples[:80]
    tr_c = [ret.get(s["query"], k=CFG["top_k"]) for s in tr]
    ds   = QADataset([s["query"] for s in tr],[s["answer"] for s in tr],tr_c,gen.tok)
    pre  = [random.uniform(0.4,0.7) for _ in tr]
    train_model(gen, ds, epochs=CFG["epochs"], bs=CFG["batch"], pre_a=pre)
    free()

    print(f"\n[Eval] ATLAS-RL model ...")
    arl  = evaluate(samples, ret, gen, pert, diag, mode="rag")

    def row(name, r):
        return {"Model":name,
                "Accuracy(F1)":  f(r["token_f1"]),
                "Hallucination": f(r["hallucination_rate"]),
                "Consistency":   f(r["consistency"]),
                "Grounding":     f(r["grounding"]),
                "ATLAS Score":   f(r["atlas_score"])}

    main_rows = [row("Baseline LLM",base), row("RAG",rag), row("ATLAS-RL",arl)]
    ptable(main_rows,"MAIN RESULTS",
           ["Model","Accuracy(F1)","Hallucination","Consistency","Grounding","ATLAS Score"])
    scsv(main_rows, CFG["res_csv"])

    print("\n[Ablation] ...")
    abl = run_ablation(samples, ret, gen, pert, diag)
    abl_rows=[{"Configuration":r["config"],"Token F1":f(r["token_f1"]),"ATLAS Score":f(r["atlas_score"])} for r in abl]
    ptable(abl_rows,"ABLATION STUDY",["Configuration","Token F1","ATLAS Score"])
    scsv(abl_rows, CFG["abl_csv"])

    all_f = base["fails"]+rag["fails"]+arl["fails"]
    fm={}
    for x in all_f: fm[x]=fm.get(x,0)+1
    tot=len(all_f)
    print(f"\n{SEP}\n  FAILURE MODE STATISTICS\n{SEP}")
    for k,v in sorted(fm.items(),key=lambda x:-x[1]):
        print(f"  {k:<25} {v:>4}  ({100*v/tot:.1f}%)")
    print(SEP)

    b,r,a=base,rag,arl
    def d(x,y): return f"{'↑' if x>y else '↓'}{abs(x-y):.4f}"
    print(f"\n{SEP}\n  KEY OBSERVATIONS\n{SEP}")
    print(f"  1. RAG vs Baseline   ATLAS {r['atlas_score']:.4f} vs {b['atlas_score']:.4f}  {d(r['atlas_score'],b['atlas_score'])}")
    print(f"  2. ATLAS-RL vs RAG   ATLAS {a['atlas_score']:.4f} vs {r['atlas_score']:.4f}  {d(a['atlas_score'],r['atlas_score'])}")
    print(f"  3. Hallucination     Base={b['hallucination_rate']:.4f} RAG={r['hallucination_rate']:.4f} ATLAS-RL={a['hallucination_rate']:.4f}")
    best=max(abl,key=lambda x:x["atlas_score"])
    worst=min(abl,key=lambda x:x["atlas_score"])
    print(f"  4. Best  ablation    '{best['config']}'  ATLAS={best['atlas_score']:.4f}")
    print(f"  5. Worst ablation    '{worst['config']}'  ATLAS={worst['atlas_score']:.4f}")
    top=max(fm,key=fm.get)
    print(f"  6. Top failure       {top}  ({100*fm[top]/tot:.1f}%)")
    print(f"\n  Runtime: {(time.time()-t0)/60:.1f} min")
    print(SEP)
    print(f"\n[DONE] {CFG['res_csv']}\n[DONE] {CFG['abl_csv']}")

main()
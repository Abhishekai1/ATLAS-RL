class RetrievalModule:
    def __init__(self):
        print("[Retrieval] Loading MiniLM ...")
        self.emb  = SentenceTransformer("sentence-transformers/all-MiniLM-L6-v2", device=DEVICE)
        self.idx  = None
        self.docs = []

    def build(self, documents):
        seen = list({d.strip()[:200] for d in documents if d.strip()})
        self.docs = seen
        embs = self.emb.encode(seen, batch_size=32, show_progress_bar=False,
                               normalize_embeddings=True).astype(np.float32)
        self.idx = faiss.IndexFlatIP(384)
        self.idx.add(embs)
        print(f"[Retrieval] {len(seen)} docs indexed")
        free()

    def get(self, query, k=2):
        if not self.idx or self.idx.ntotal == 0:
            return []
        q = self.emb.encode([query], normalize_embeddings=True).astype(np.float32)
        _, ii = self.idx.search(q, min(k, len(self.docs)))
        return [self.docs[i] for i in ii[0] if i < len(self.docs)]


class Perturber:
    def paraphrase(self, q):
        w = q.split()
        if len(w) < 3: return q
        i = random.randint(0, len(w)-2)
        w[i], w[i+1] = w[i+1], w[i]
        return " ".join(w).lower()

    def corrupt(self, docs, noise, ratio=0.5):
        out = list(docs)
        for i in range(max(1, int(len(out)*ratio))):
            if noise and i < len(out):
                out[i] = random.choice(noise)
        return out


class GenModule:
    def __init__(self, model_name="google/flan-t5-base"):
        print("[Gen] Loading flan-t5-base ...")
        self.tok = AutoTokenizer.from_pretrained(model_name)
        self.model = AutoModelForSeq2SeqLM.from_pretrained(model_name, torch_dtype=torch.float32).to(DEVICE)
        self.model.eval()

    def _prompt(self, q, docs):
        ctx = " ".join(docs)[:250]
        return f"Context: {ctx}\nQuestion: {q}\nAnswer:"

    def gen_logits(self, q, docs):
        prompt = self._prompt(q, docs)
        enc = self.tok(prompt, return_tensors="pt", max_length=350,
                       truncation=True).to(DEVICE)
        with torch.no_grad():
            out = self.model.generate(
                **enc, max_new_tokens=40,
                output_scores=True, return_dict_in_generate=True,
                num_beams=1, do_sample=False,
            )
        ans = self.tok.decode(out.sequences[0], skip_special_tokens=True)
        tlp = torch.zeros(1)
        if out.scores:
            try:
                scores = torch.stack([s.float().cpu() for s in out.scores], dim=0)
                lp     = torch.log_softmax(scores, dim=-1)
                ids    = out.sequences[0][1:scores.shape[0]+1].cpu()
                ids    = ids.clamp(0, lp.shape[-1]-1)
                T      = min(lp.shape[0], len(ids))
                if T > 0:
                    tlp = lp[torch.arange(T), ids[:T]]
            except Exception:
                tlp = torch.zeros(1)
        del enc, out; free()
        return ans, tlp

    def gen_base(self, q):
        enc = self.tok(f"Question: {q}\nAnswer:", return_tensors="pt",
                       max_length=350, truncation=True).to(DEVICE)
        with torch.no_grad():
            out = self.model.generate(**enc, max_new_tokens=40,
                                      num_beams=1, do_sample=False)
        ans = self.tok.decode(out[0], skip_special_tokens=True)
        del enc, out; free()
        return ans


class NLIModule:
    def __init__(self):
        print("[NLI] Loading bart-large-mnli ...")
        name = "facebook/bart-large-mnli"
        self.tok   = AutoTokenizer.from_pretrained(name)
        self.model = AutoModelForSequenceClassification.from_pretrained(
            name, torch_dtype=torch.float32).to(DEVICE)
        self.model.eval()

    def _run(self, p, h):
        enc = self.tok(p[:350], h[:150], return_tensors="pt",
                       truncation=True, max_length=512).to(DEVICE)
        with torch.no_grad():
            logits = self.model(**enc).logits[0].float().cpu()
        probs = torch.softmax(logits, dim=-1).numpy()
        del enc, logits; free()
        return probs

    def entail(self, p, h):  return float(self._run(p,h)[2])
    def contra(self, p, h):  return float(self._run(p,h)[0])


class Diagnostics:
    def __init__(self, nli):
        self.nli = nli

    def consistency(self, a, pa):
        if not a.strip() or not pa.strip(): return 0.0
        return float((self.nli.entail(a,pa) + self.nli.entail(pa,a)) / 2)

    @staticmethod
    def uncertainty(tlp):
        if tlp is None or tlp.numel()==0: return 0.5
        v = -tlp.float().mean().item()
        return float(np.clip(1/(1+np.exp(-v+3)), 0, 1))

    def grounding(self, a, docs):
        if not docs or not a.strip(): return 0.0
        return self.nli.entail(" ".join(docs)[:350], a)

    def hallucination(self, a, docs):
        if not docs or not a.strip(): return 0.0
        return self.nli.contra(" ".join(docs)[:350], a)

    def run(self, a, pa, docs, tlp=None):
        return dict(
            consistency   = self.consistency(a, pa),
            uncertainty   = self.uncertainty(tlp),
            grounding     = self.grounding(a, docs),
            hallucination = self.hallucination(a, docs),
        )

    @staticmethod
    def failure(d):
        if d["hallucination"] > 0.5: return "hallucination"
        if d["grounding"]     < 0.3: return "retrieval_error"
        if d["consistency"]   < 0.4: return "inconsistency"
        return "none"


def kl(lp, lq):
    n = min(len(lp), len(lq))
    if n == 0: return 0.0
    a = lp[:n].float().clamp(-20,0)
    b = lq[:n].float().clamp(-20,0)
    return float(max((a.exp()*(a-b)).mean().item(), 0.0))

def reliability(tlp, ptlp, sc=5.0):
    return float(np.clip(1 - 1/(1+np.exp(-kl(tlp,ptlp)/sc+1)), 0, 1))

def atlas(R, G, C, U):
    w = CFG["w"]
    return float(np.clip(w["alpha"]*R + w["beta"]*G + w["gamma"]*C - w["delta"]*U, 0, 1))

def atlas_abl(R, G, C, U, nk=False, nc=False, ng=False):
    return atlas(0.5 if nk else R, 0.5 if ng else G, 0.5 if nc else C, U)

def tf1(p, r):
    ps, rs = set(p.lower().split()), set(r.lower().split())
    if not rs: return 0.0
    c = ps & rs
    if not c: return 0.0
    pr = len(c)/len(ps) if ps else 0
    rc = len(c)/len(rs)
    return 2*pr*rc/(pr+rc) if (pr+rc) > 0 else 0.0

def agg(preds, refs, diags, scores):
    return dict(
        token_f1           = float(np.mean([tf1(p,r) for p,r in zip(preds,refs)])),
        hallucination_rate = float(np.mean([d["hallucination"] for d in diags])),
        consistency        = float(np.mean([d["consistency"]   for d in diags])),
        grounding          = float(np.mean([d["grounding"]     for d in diags])),
        uncertainty        = float(np.mean([d["uncertainty"]   for d in diags])),
        atlas_score        = float(np.mean(scores)),
    )


